A specific process that deals with errors in a manageable and predictable way.

In Ruby, when an exception (aka error) is raised, 
it halts execution unless there is code in place to rescue the exception.

Exception hangling solves the problem of exceptional conditions a program might encounter:
a user enters bad information or a file-manipulating process gets corrupted.
It tells your program what to do when it runs into these errors.

Ruby has an `Exception` class a built in way of handling certains errors. 
It has a syntactic structure using the reserved words `begin`, `rescue`, and `end` .. 
